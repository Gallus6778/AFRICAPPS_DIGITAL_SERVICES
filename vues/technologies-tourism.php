                        <h3 id="authenticity">DOCS authenticity  : Application de vérification d'authenticité des documents</h3>
                        <p>
                            C’est depuis très longtemps que le Cameroun fait face au phénomène dit de « faux diplômes », un fléau endémique qui frappe la quasi-totalité des administrations au point où certaines sont devenues comme des déversoirs de faux parchemins. Nous n’allons pas citer nommément les administrations concernées ici, mais elles sont nombreuses surtout dans le secteur parapublic.<br/>
                            « Après investigations approfondies, on a constaté que le diplôme présenté au moment du recrutement de nombreux agents dans plusieurs administrations publiques est faux », précise un contrôleur en service au Contrôle supérieur de l’Etat, qui a enquêté longtemps dans un grand nombre de services publics. Dans ce même registre, 300 élèves -professeurs avaient été recalés en 2012 pour faux diplômes à l’école Nationale supérieure de Maroua. Dans une interview accordée au quotidien national bilingue Cameroon Tribune, Pr Jacques Fame Ndongo, ministre de l’enseignement supérieur déclarait que depuis 2005,  six cent quatre-vingt-deux (682) faux diplômes sont tombés dans les mailles des services de contrôle de son département ministériel. Pas moins<br/>
                            Le trafic de faux diplômes est un commerce mondial. Sur Internet, on peut tout acheter. En quelques clics, vous pouvez devenir, sur papier, un as de la finance, un médecin ou un professeur de français. Et ce, dans n'importe quelle université au monde. Donc, c’est un phénomène qui ne laisse aucun pays indifférent et pour illustration, le Tchad a été à l’origine d’un scandale de près de plus de trois mille (3000) faux diplômes en Août 2019 dans la fonction publique.<br/>
                            Ce constat alarmant nous a donc donné des motivations claires : Concevoir et mettre en œuvre une application innovante pour l’édition, le cryptage et l’authentification des documents. Il s’agit en réalité d’une application de génération de QR code (code barre en deux dimensions) par laquelle nous demandons à faire transiter tout nouveau document juste avant l’impression. Une fois le document importé dans cette application, il est tout de suite enregistré, crypté et avec un QR code appliqué dessus avant de l’imprimé ; et tout ceci en une fraction de seconde et un clic. Ensuite, l’authentification se fera tout simplement avec un téléphone portable connecté dans lequel on a installé un lecteur de QR code au préalable quel que soit votre position géographique et disponible 24/24.<br/>
                            La simplicité et l’efficacité de cette application nous augurent un avenir meilleur pour ce qui est de l’avènement des faux diplômes, les faux passeports, les faux visas, les fausses lettres d’acceptation, les faux certificats d’intégration et plus encore… Nous sommes convaincu, dans un avenir très proche, de combattre avec force pourquoi pas, d’éradiquer  même complètement les faux documents pour toutes les institutions qui voudront bien coopérer avec nous.
                        </p>

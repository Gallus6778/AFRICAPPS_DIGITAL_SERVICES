                        <h3 id="sig-web">SIG-WEB Pollution  : Application pour bien etre environnemental</h3>
                        <p>
                            La pollution de l'air est une altération de la qualité de l'air pouvant être caractérisée par des mesures de polluants chimiques, biologiques ou physiques. Elle peut avoir des conséquences préjudiciables à la santé humaine,  aux êtres vivants, au climat, ou aux biens matériels.<br/>
                            En 2018, l'OMS évalue à 7 millions le nombre de personnes qui meurent dans le monde parce qu’elles respirent un air trop chargé en particules fines (CO, CO2, NO, NO2, PM10 and PM2.5), dont 4,2 millions de victimes de la pollution de l'air extérieur et 3,8 millions pour celle de l'air intérieur. 91 % de la population mondiale est exposée quotidiennement à l'air contenant de hauts niveaux de polluants.<br/>
                            Alors, nous avons mis sur pied un outil technologique très innovant d’aide à la détection, l’évaluation en teneur et la mise en évidence des gaz polluants responsables d’un certain nombre de maladies  cardio-respiratoires. Il s’agit en fait d’un réseau de poly-capteurs rassemblés et montés par notre startup, et connecté à une application web qui traite et présente les données sous forme de tableau et de courbes mettant en évidence la cartographie, les valeurs et les types de gaz polluants, mais aussi les statistiques des hôpitaux et des patients donc la cause probable est la pollution. Cette application web va jusqu’à détecter des seuils à partir desquels, un SMS automatique est généré et envoyé avec toutes les précisions possibles sur les types de particules fines et leurs coordonnées géographiques. Ceci nous permettra d’avoir une idée précise sur les zones à forte teneur en gaz polluants et ensuite de pouvoir y interpeller les acteurs du domaine de l’environnement dans notre pays, mais surtout les décideurs de la république.
                        </p>
